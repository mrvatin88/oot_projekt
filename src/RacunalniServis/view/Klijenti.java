    private void btnSpremiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpremiActionPerformed
        // TODO add your handling code here:
        String imeKlijenta = txtIme.getText();
        String prezimeKlijenta = txtPrezime.getText();
        String eMail = txtEMail.getText();
        Integer brojTelefona = Integer.parseInt(txtTel.getText());
        
        Klijent klijent = new Klijent();
        
        klijent.setImeKlijenta(imeKlijenta);
        klijent.setPrezimeKlijenta(prezimeKlijenta);
        klijent.seteMail(eMail);
        klijent.setBrojTelefona(brojTelefona);
        
        control.unosKlijenta(klijent);
        
        txtIme.setText(" ");
        txtPrezime.setText(" ");
        txtEMail.setText(" ");
        txtTel.setText(" ");
    }//GEN-LAST:event_btnSpremiActionPerformed

