package RacunalniServis.model;

public class Racun {

    public Integer sifraRacuna;

    public String izvrseneUsluge;

    public Integer utrosenoVrijeme;

    public Double ukupnaCijena;

    public Integer getSifraRacuna() {
        return sifraRacuna;
    }

    public void setSifraRacuna(Integer sifraRacuna) {
        this.sifraRacuna = sifraRacuna;
    }

    public String getIzvrseneUsluge() {
        return izvrseneUsluge;
    }

    public void setIzvrseneUsluge(String izvrseneUsluge) {
        this.izvrseneUsluge = izvrseneUsluge;
    }

    public Integer getUtrosenoVrijeme() {
        return utrosenoVrijeme;
    }

    public void setUtrosenoVrijeme(Integer utrosenoVrijeme) {
        this.utrosenoVrijeme = utrosenoVrijeme;
    }

    public Double getUkupnaCijena() {
        return ukupnaCijena;
    }

    public void setUkupnaCijena(Double ukupnaCijena) {
        this.ukupnaCijena = ukupnaCijena;
    }
    
    
}
